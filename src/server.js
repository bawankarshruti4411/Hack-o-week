require('dotenv').config();
const app = require('./app');
const prisma = require('./config/database');

const PORT = process.env.PORT || 5000;
const NODE_ENV = process.env.NODE_ENV || 'development';

// в”Ђв”Ђв”Ђ Connect Database & Start Server в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
const startServer = async () => {
  try {
    // Connect to PostgreSQL via Prisma
    await prisma.$connect();
    console.log('вњ… PostgreSQL Database connected via Prisma');

    // Start HTTP server
    const server = app.listen(PORT, () => {
      console.log('\nв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ');
      console.log('   рџЋ“  Student Management API');
      console.log('в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ');
      console.log(`  вњ…  Server    : http://localhost:${PORT}`);
      console.log(`  рџ“„  Docs      : http://localhost:${PORT}/api-docs`);
      console.log(`  рџЏҐ  Health    : http://localhost:${PORT}/api/v1/health`);
      console.log(`  рџЊЌ  Env       : ${NODE_ENV}`);
      console.log('в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\n');
    });

    // в”Ђв”Ђв”Ђ Graceful Shutdown в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
    const shutdown = (signal) => {
      console.log(`\nвљ пёЏ  ${signal} received. Gracefully shutting down...`);
      server.close(() => {
        console.log('вњ…  HTTP server closed.');
        process.exit(0);
      });

      // Force close after 10s
      setTimeout(() => {
        console.error('вќЊ  Forced shutdown after timeout.');
        process.exit(1);
      }, 10000);
    };

    process.on('SIGTERM', () => shutdown('SIGTERM'));
    process.on('SIGINT', () => shutdown('SIGINT'));

  } catch (error) {
    console.error('вќЊ  Server failed to start:', error.message);
    process.exit(1);
  }
};

// в”Ђв”Ђв”Ђ Handle Unhandled Errors в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
process.on('unhandledRejection', (reason, promise) => {
  console.error('рџ”Ґ  Unhandled Rejection at:', promise, '\n   Reason:', reason);
  process.exit(1);
});

process.on('uncaughtException', (error) => {
  console.error('рџ”Ґ  Uncaught Exception:', error.message);
  process.exit(1);
});

startServer();
